# Crie um algoritmo que leia um numero inteiro e mostre seu dobro, triplo e a raiz quadrada.

numero = int(input('Digite um numero para fazer a conta de seu dobro, triplo e raiz quadrada: '))
dobro = numero * 2 
triplo = numero * 3 
raizquadrada = numero ** 0.5 
print (f'O dobro do número {numero} é {dobro}')
print (f'O triplo do número {numero} é {triplo}')
print (f'A raiz quadrada de {numero} é {raizquadrada}')



